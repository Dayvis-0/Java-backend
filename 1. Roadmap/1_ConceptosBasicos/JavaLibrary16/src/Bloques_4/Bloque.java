package Bloques_4; 
/* Un boque es un grupo de cero o mas declaraciones entre aparatos ortopedicos equilibrados y se puede usar 
en cualquier lugar donde se permita una sola declaracion*/
public class Bloque  {
    public static void main(String[] args) {
        boolean conditon = true;
        if (conditon) {
            System.out.println("Condition is true");
        }
        else {
            System.out.println("Condicion is false");
        }
    }
}
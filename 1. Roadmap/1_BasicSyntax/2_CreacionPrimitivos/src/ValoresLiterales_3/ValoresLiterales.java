package ValoresLiterales_3;
/*Es posible que haya notado que el new la palabra clave no se usa al inicializar una variable de tipo primitivo.
Los primitivos son tipos de datos especiales integrados en el lenguaje; no son objetos creados apartir de una clase.*/
public class ValoresLiterales {
    public static void main(String[] args) {
        /*Un literal es la representacion del codigo fuente de un valor fijo; los literales se representan 
        directamente en su codigo sin requerir calculo   */
        boolean result = true;
        char capitali = 'C';
        byte b = 100;
        short s = 10000;
        int a_int = 1000;
        
    }
}
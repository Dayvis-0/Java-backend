package Declaraciones_3;
/*Las declaraciones son aproximadamente equivalente a las oraciones en idiomas naturale.*/
class Declaraciones_1 {
    public static void main(String[] args) {
        double aValue;
        // declaracion de asignacion 
        aValue = 8933.234;
        // declaracion de incremento 
        aValue++;
        // asignacion de invocacion de metodo
        System.out.println("Hello world");
        // asignacion de creacion de objetos
        Bicycle myBike = new Bicycle();
    }
}

